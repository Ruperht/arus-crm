    private void btnEditarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarServicioActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaServicios.getSelectedRow();

        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Selecciona un servicio de la tabla antes de editar.",
                    "Ningún servicio seleccionado",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id = (int) tablaServicios.getValueAt(filaSeleccionada, 0);

        ServicioDAO dao = new ServicioDAO();
        List<Servicio> servicios = dao.listar();

        Servicio servicioSeleccionado = null;
        for (Servicio s : servicios) {
            if (s.getId() == id) {
                servicioSeleccionado = s;
                break;
            }
        }

        if (servicioSeleccionado != null) {
            FormularioServicio form = new FormularioServicio(this, servicioSeleccionado);
            form.setVisible(true);

            if (form.isServicioGuardado()) {
                cargarServicios();
            }
        }
    }//GEN-LAST:event_btnEditarServicioActionPerformed

