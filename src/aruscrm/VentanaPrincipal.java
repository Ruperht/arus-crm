    private void btnEditarContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarContratoActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaContratos.getSelectedRow();

        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Selecciona un contrato de la tabla antes de editar.",
                    "Ningún contrato seleccionado",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id = (int) tablaContratos.getValueAt(filaSeleccionada, 0);

        ContratoDAO dao = new ContratoDAO();
        List<Contrato> contratos = dao.listar();

        Contrato contratoSeleccionado = null;
        for (Contrato c : contratos) {
            if (c.getId() == id) {
                contratoSeleccionado = c;
                break;
            }
        }

        if (contratoSeleccionado != null) {
            FormularioContrato form = new FormularioContrato(this, contratoSeleccionado);
            form.setVisible(true);

            if (form.isContratoGuardado()) {
                cargarContratos();
            }
        }
    }//GEN-LAST:event_btnEditarContratoActionPerformed

