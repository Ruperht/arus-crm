    private void cargarClientes() {
        DefaultTableModel modelo = (DefaultTableModel) tablaClientes.getModel();
        modelo.setRowCount(0);

        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        ClienteDAO dao = new ClienteDAO();
        List<Cliente> clientes = dao.listar();

        for (Cliente c : clientes) {
            Object[] fila = new Object[]{
                c.getId(),
                c.getNombre(),
                c.getEmail(),
                c.getTelefono(),
                c.getEmpresa(),
                c.getOrigenLead(),
                c.getFechaAlta() != null ? c.getFechaAlta().format(formato) : ""
            };
            modelo.addRow(fila);
        }
    }

    private void cargarServicios() {
        DefaultTableModel modelo = (DefaultTableModel) tablaServicios.getModel();
        modelo.setRowCount(0);

        ServicioDAO dao = new ServicioDAO();
        List<Servicio> servicios = dao.listar();

        for (Servicio s : servicios) {
            Object[] fila = new Object[]{
                s.getId(),
                s.getNombre(),
                s.getDescripcion(),
                s.getPrecioBase(),
                s.getTipo()
            };
            modelo.addRow(fila);
        }
    }

    private void cargarContratos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaContratos.getModel();
        modelo.setRowCount(0);

        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        ContratoDAO dao = new ContratoDAO();
        List<Contrato> contratos = dao.listar();

        for (Contrato c : contratos) {
            Object[] fila = new Object[]{
                c.getId(),
                c.getNombreCliente(),
                c.getNombreServicio(),
                c.getFechaInicio().format(formato),
                c.getFechaFin() != null ? c.getFechaFin().format(formato) : "",
                c.getEstado(),
                c.getPrecioFinal(),
                c.isFacturado() ? "Sí" : "No",
                c.getNumeroFactura() != null ? c.getNumeroFactura() : ""
            };
            modelo.addRow(fila);
        }
    }

